
#!/bin/bash
# Script com exemplos de comandos básicos

# Listar arquivos no diretório atual
echo "Listando arquivos no diretório atual:"
ls

# Navegar para um diretório específico
echo "Mudando para o diretório /home:"
cd /home

# Criar um novo diretório
echo "Criando o diretório 'novos_projetos':"
mkdir novos_projetos

# Cria um arquivo chamado arquivo.txt
echo "Dance, Dance" > arquivo.txt

# Exibir o conteúdo de um arquivo
echo "Exibindo o conteúdo do arquivo 'arquivo.txt':"
cat arquivo.txt

